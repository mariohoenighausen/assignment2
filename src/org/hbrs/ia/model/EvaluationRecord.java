package org.hbrs.ia.model;

public class EvaluationRecord {
    private int erid;
    private int actualValue;
    private int year;
    private int sid;
    private int goalID;

    public int getErid() {
        return erid;
    }

    public int getActualValue() {
        return actualValue;
    }

    public int getYear() {
        return year;
    }

    public int getSid() {
        return sid;
    }

    public int getGoalID() {
        return goalID;
    }
}
